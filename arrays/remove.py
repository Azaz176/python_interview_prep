fruits = ["apple", "banana", "cherry"]
fruits.pop()  # Removes the last element
print(fruits)  # Output: ["apple", "banana"]

fruits.pop(1)  # Removes the element at index 1
print(fruits)  # Output: ["apple"]

fruits.remove("apple")
print(fruits)  # Output: []

fruits=["apple", "banana"]
fruits.clear()
print(fruits)

